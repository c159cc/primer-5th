
#include <cstring>
#include <iostream>
#include <string>
#include <vector>
using namespace std;
/*

> Write a program to print the size of each of the built-in types.
 */
int main() {

    cout << "size of char:" << sizeof(char) << endl;
    cout << "size of short:" << sizeof(short) << endl;
    cout << "size of int:" << sizeof(int) << endl;
    cout << "size of float:" << sizeof(float) << endl;
    cout << "size of double:" << sizeof(double) << endl;
    cout << "size of long double:" << sizeof(long double) << endl;
    cout << "size of bool:" << sizeof(bool) << endl;
    cout << "size of long:" << sizeof(long) << endl;
    cout << "size of long long:" << sizeof(long long) << endl;
    cout << "size of unsigned char:" << sizeof(unsigned char) << endl;
    cout << "size of unsigned int:" << sizeof(unsigned int) << endl;
    cout << "size of unsigned short:" << sizeof(unsigned short) << endl;
    cout << "size of unsigned long:" << sizeof(unsigned long) << endl;
    cout << "size of unsigned long long:" << sizeof(unsigned long long) << endl;
    cout << "size of wchar_t:" << sizeof(wchar_t) << endl;
    cout << "size of char16_t:" << sizeof(char16_t) << endl;
    cout << "size of char32_t:" << sizeof(char32_t) << endl;
    cout << "size of size_t:" << sizeof(size_t) << endl;
    cout << "size of ptrdiff_t:" << sizeof(ptrdiff_t) << endl;
    cout << "size of nullptr_t:" << sizeof(nullptr_t) << endl;
    cout << "size of int8_t:" << sizeof(int8_t) << endl;
    cout << "size of int16_t:" << sizeof(int16_t) << endl;
    cout << "size of int32_t:" << sizeof(int32_t) << endl;
    cout << "size of int64_t:" << sizeof(int64_t) << endl;
    cout << "size of uint8_t:" << sizeof(uint8_t) << endl;
    cout << "size of uint16_t:" << sizeof(uint16_t) << endl;
    cout << "size of uint32_t:" << sizeof(uint32_t) << endl;
    cout << "size of uint64_t:" << sizeof(uint64_t) << endl; 

    return 0;
}
